package org.tnsindia.comparatordemo;

public class Employee {
	private int id;
	private String name;
	//setter and getters method
	public int getAge() {
		return id;
	}
	public void setAge(int age) {
		this.id = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
